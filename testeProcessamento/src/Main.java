public class Main {
    public static void main(String[] args) throws Exception {
        int x, y;
        double z;

        double b, B, h, area;

        int c, d;
        double resultado;

        x = 5;
        y = 2 * x;
        z = 2 * x;

        b = 6.0;
        B = 8.0;
        h = 5.0;

        area = (b + B) / 2.0 * h;

        c = 5;
        d = 2;
        resultado = (double) c / d;

        System.out.println(x);
        System.out.println(y);
        System.out.println(z);

        System.out.println(area);

        System.out.println(resultado);
    }
}
